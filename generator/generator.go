func generateRepository(cfg *schema.ProjectConfig, pkgDir string) error {
	var b strings.Builder

	b.WriteString(fmt.Sprintf(`// Package %s - Auto-generated repository layer
// DO NOT EDIT - Generated by restful-api-gen-go
package %s

import (
	"context"
	"database/sql"
	"fmt"
	"reflect"
	"strings"

	"github.com/jmoiron/sqlx"
)

`, cfg.PackageName, cfg.PackageName))

	for _, t := range cfg.Tables {
		pkName := t.PrimaryKey
		pkType := "int64"
		for _, c := range t.Columns {
			if c.IsPrimaryKey {
				pkType = c.GoType
				break
			}
		}

		b.WriteString(fmt.Sprintf(`// %sRepository handles database operations for %s
type %sRepository struct {
	db *sqlx.DB
}

func New%sRepository(db *sqlx.DB) *%sRepository {
	return &%sRepository{db: db}
}

`, t.Alias, t.Name, t.Alias, t.Alias, t.Alias, t.Alias))

		// GetAll
		b.WriteString(fmt.Sprintf(`// GetAll retrieves all %s records with pagination
func (r *%sRepository) GetAll(ctx context.Context, page, pageSize int) ([]%s, int, error) {
	var total int
	if err := r.db.QueryRowContext(ctx, "SELECT COUNT(*) FROM %s").Scan(&total); err != nil {
		return nil, 0, fmt.Errorf("count %%s: %%w", "%s", err)
	}
	offset := (page - 1) * pageSize
	rows, err := r.db.QueryxContext(ctx, "SELECT * FROM %s ORDER BY %s OFFSET ? ROWS FETCH NEXT ? ROWS ONLY", offset, pageSize)
	if err != nil {
		return nil, 0, fmt.Errorf("select %%s: %%w", "%s", err)
	}
	defer rows.Close()

	var items []%s
	for rows.Next() {
		var item %s
		if err := rows.StructScan(&item); err != nil {
			return nil, 0, fmt.Errorf("scan %%s: %%w", "%s", err)
		}
		items = append(items, item)
	}
	if items == nil {
		items = []%s{}
	}
	return items, total, nil
}

`, t.Alias, t.Alias, t.Alias, t.Name, t.Name, t.Name, pkName, t.Name, t.Alias, t.Alias, t.Name, t.Alias))

		// GetByID
		b.WriteString(fmt.Sprintf(`// GetByID retrieves a single %s by %s
func (r *%sRepository) GetByID(ctx context.Context, id %s) (*%s, error) {
	var item %s
	if err := r.db.GetContext(ctx, &item, "SELECT * FROM %s WHERE %s = ?", id); err != nil {
		if err == sql.ErrNoRows {
			return nil, nil
		}
		return nil, fmt.Errorf("get %%s by %%s: %%w", "%s", "%s", err)
	}
	return &item, nil
}

`, t.Alias, pkName, t.Alias, pkType, t.Alias, t.Alias, t.Name, pkName, t.Name, pkName))

		// Create
		autoInc := false
		for _, c := range t.Columns {
			if c.IsAutoIncrement {
				autoInc = true
				break
			}
		}

		b.WriteString(fmt.Sprintf(`// Create inserts a new %s record
func (r *%sRepository) Create(ctx context.Context, item *%s) error {
	cols, vals, args := extractInsertFields(item)
	query := fmt.Sprintf("INSERT INTO %%s (%%s) VALUES (%%s)", "%s", cols, vals)
`, t.Alias, t.Alias, t.Alias, t.Name))

		if autoInc {
			b.WriteString(fmt.Sprintf(`	if err := r.db.QueryRowContext(ctx, query+"; SELECT CAST(SCOPE_IDENTITY() AS BIGINT)", args...).Scan(&item.%s); err != nil {
		return fmt.Errorf("insert %%s: %%w", "%s", err)
	}
	return nil
}

`, toPascal(pkName), t.Name))
		} else {
			b.WriteString(fmt.Sprintf(`	_, err := r.db.ExecContext(ctx, query, args...)
	if err != nil {
		return fmt.Errorf("insert %%s: %%w", "%s", err)
	}
	return nil
}

`, t.Name))
		}

		// Update
		b.WriteString(fmt.Sprintf(`// Update modifies an existing %s record
func (r *%sRepository) Update(ctx context.Context, id %s, item *%s) error {
	sets, args := extractUpdateFields(item)
	args = append(args, id)
	query := fmt.Sprintf("UPDATE %%s SET %%s WHERE %s = ?", "%s", sets)
	result, err := r.db.ExecContext(ctx, query, args...)
	if err != nil {
		return fmt.Errorf("update %%s: %%w", "%s", err)
	}
	rows, _ := result.RowsAffected()
	if rows == 0 {
		return fmt.Errorf("%%s with %s=%%v not found", "%s", id)
	}
	return nil
}

`, t.Alias, t.Alias, pkType, t.Alias, pkName, t.Name, t.Name, t.Name, pkName))

		// Delete
		b.WriteString(fmt.Sprintf(`// Delete removes a %s record
func (r *%sRepository) Delete(ctx context.Context, id %s) error {
`, t.Alias, t.Alias, pkType))

		if t.SoftDelete {
			b.WriteString(fmt.Sprintf(`	if _, err := r.db.ExecContext(ctx, "UPDATE %s SET deleted_at = GETDATE() WHERE %s = ?", id); err != nil {
		return fmt.Errorf("soft-delete %%s: %%w", "%s", err)
	}
	return nil
}

`, t.Name, pkName, t.Name))
		} else {
			b.WriteString(fmt.Sprintf(`	result, err := r.db.ExecContext(ctx, "DELETE FROM %s WHERE %s = ?", id)
	if err != nil {
		return fmt.Errorf("delete %%s: %%w", "%s", err)
	}
	rows, _ := result.RowsAffected()
	if rows == 0 {
		return fmt.Errorf("%%s with %s=%%v not found", "%s", id)
	}
	return nil
}

`, t.Name, pkName, t.Name, pkName, t.Name))
		}
	}

	// Write helper functions
	b.WriteString(`// --- Runtime helpers (reflect-based) ---

// extractInsertFields returns column names, placeholders, and values
func extractInsertFields(item interface{}) (string, string, []interface{}) {
	v := reflect.ValueOf(item).Elem()
	t := v.Type()
	var cols, placeholders []string
	var args []interface{}
	for i := 0; i < t.NumField(); i++ {
		field := t.Field(i)
		dbTag := field.Tag.Get("db")
		if dbTag == "" {
			continue
		}
		// Skip auto-increment PKs
		val := v.Field(i)
		// Skip zero values for auto-increment fields
		if !val.IsValid() {
			continue
		}
		cols = append(cols, dbTag)
		placeholders = append(placeholders, "?")
		args = append(args, val.Interface())
	}
	return strings.Join(cols, ", "), strings.Join(placeholders, ", "), args
}

// extractUpdateFields returns SET clause and args (skipping PK)
func extractUpdateFields(item interface{}) (string, []interface{}) {
	v := reflect.ValueOf(item).Elem()
	t := v.Type()
	var sets []string
	var args []interface{}
	for i := 0; i < t.NumField(); i++ {
		field := t.Field(i)
		dbTag := field.Tag.Get("db")
		if dbTag == "" {
			continue
		}
		val := v.Field(i)
		if !val.IsValid() {
			continue
		}
		sets = append(sets, dbTag+" = ?")
		args = append(args, val.Interface())
	}
	return strings.Join(sets, ", "), args
}
`)

	return writeGoFile(pkgDir, "repository.go", b.String())
}

