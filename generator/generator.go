func buildConnStr(db schema.DBConfig) string {
	if db.ConnStr != "" {
		return db.ConnStr
	}
	user := db.User
	pass := db.Password
	host := db.Host
	if host == "" {
		host = "localhost"
	}
	port := db.Port
	if port == 0 {
		port = 1433
	}
	dbName := db.Database
	return fmt.Sprintf("sqlserver://%s:%s@%s:%d?database=%s&encrypt=disable", user, pass, host, port, dbName)
}

// --- Model (template-based) ---

func generateModel(cfg *schema.ProjectConfig, pkgDir string) error {
	// Build alias lookup: table name -> struct alias
	aliasMap := make(map[string]string)
	for _, t := range cfg.Tables {
		aliasMap[t.Name] = t.Alias
	}

	var b strings.Builder
	b.WriteString(fmt.Sprintf(`// Package %s - Auto-generated models
// DO NOT EDIT - Generated by restful-api-gen-go
package %s

`, cfg.PackageName, cfg.PackageName))

	if hasTimeFields(cfg.Tables) {
		b.WriteString("import \"time\"\n\n")
	}

	for _, t := range cfg.Tables {
		// Struct definition
		b.WriteString(fmt.Sprintf(`// %s represents the %s table
type %s struct {
`, t.Alias, t.Name, t.Alias))
		for _, c := range t.Columns {
			jsonTag := c.Name
			if c.Nullable {
				jsonTag += ",omitempty"
			}
			desc := c.Description
			if desc == "" {
				desc = c.Name
			}
			b.WriteString(fmt.Sprintf("	// %s\n	%s %s `json:\"%s\" db:\"%s\"`\n",
				desc, toPascal(c.Name), c.GoType, jsonTag, c.Name))
		}
		b.WriteString("}\n\n")

		// TableName method
		b.WriteString(fmt.Sprintf(`func (%s) TableName() string { return "%s" }

`, t.Alias, t.Name))

		// Relations
		for _, r := range t.Relations {
			if r.Type == "has_many" {
				// Look up the alias for the related table
				relAlias := aliasMap[r.Table]
				if relAlias == "" {
					relAlias = toPascal(r.Table)
				}
				b.WriteString(fmt.Sprintf(`// %sRelations holds related records
type %sRelations struct {
	%s []%s `+"`"+`json:"%s,omitempty"`+"`"+`
}

`, t.Alias, t.Alias, relAlias, relAlias, r.Table))
			}
		}
	}

	return writeGoFile(pkgDir, "model.go", b.String())
}

